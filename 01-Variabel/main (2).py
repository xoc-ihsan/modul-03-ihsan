# variabel: adalah tempat menyimpan data

# menaruh data / assignment data
a = 10
x = 5
panjang = 1000

# memanggil variabel
print("Nilai a : ", a)
print("Nilai x : ", x)
print("Nilai panjang : ", panjang)

# penamaan variabel
nilai_y = 15  # dengan underscore
juta10 = 10000000  # model camel
nilaiAngka = 100  # model camel

# memanggil variabel
print("Nilai nilai_y : ", nilai_y)
print("Nilai juta10 : ", juta10)
print("nilaiAngka : ", nilaiAngka)

# mengubah isi variabel
print("Nilai a : ", a)  # nilai a awal = 10
a = 7
print("Nilai a : ", a)  # nilai a diubah = 7

print("Nilai x : ", x)  # nilai x awal = 5
x = 3
print("Nilai x : ", x)  # nilai x diubah = 3
